_BY_ID = {m["id"]: m for m in TEAM}


def get_member(member_id: str) -> dict[str, Any] | None:
    return _BY_ID.get((member_id or "").strip().lower())


def public_team() -> list[dict[str, Any]]:
    """Roster for the client — no persona internals."""
    return [
        {
            "id": m["id"],
            "name": m["name"],
            "title": m["title"],
            "specialty": m["specialty"],
            "avatar": f"/team/{m['id']}.png",
            "metrics": m.get("metrics", []),
        }
        for m in TEAM
    ]


def member_profile(member_id: str) -> dict[str, Any] | None:
    """Full public profile for one member — résumé, cover letter, metrics.

    No persona internals. Metrics/résumé are illustrative character content for an
    AI persona; the ``illustrative`` flag lets the client label them honestly.
    """
    m = get_member(member_id)
    if not m:
        return None
    return {
        "id": m["id"],
        "name": m["name"],
        "title": m["title"],
        "specialty": m["specialty"],
        "avatar": f"/team/{m['id']}.png",
        "resume": m.get("resume", {}),
        "cover_letter": m.get("cover_letter", ""),
        "metrics": m.get("metrics", []),
        "illustrative": True,
    }


def persona_context(member_id: str) -> str:
    """The instruction prepended to the user context so ARIA works AS this pro."""
    m = get_member(member_id)
    if not m:
        return ""
    return (
        f"[Actúa como {m['name']}, {m['title']} del equipo de ARIA. {m['persona']} "
        f"Mantén tu especialidad y entrega el trabajo terminado.]"
    )
